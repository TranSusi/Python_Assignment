# Using Loop, 'for..in'

# Using Xrange to iterate over a sequence of numbers
for y in range(6):
    print(y) #Prints out 0,1,2,3,4
    
for y in range(4,7):
    print(y) #prints put (4,5,6)

    
for x in range(4,10,2):
    print(x) #prints out 4,6,8

